# ✅ Ejercicio 1: Listas con .append() y .remove()
# 1️⃣ lista con 3 productos iniciales
Compras = ["aroz", "carne", "ensalada"]
print(Compras[0])
# 2️⃣ Agregar un producto
Compras.append("pasta")
# 3️⃣ Eliminar un producto
Compras.remove("carne")
# 4️⃣ Imprimir la lista final
print("\nLista final de la compra:")
for i in Compras:
    print(f"- {i}")




