#!üìò Ejemplo b√°sico 2: leer un archivo

archivo = open("saludo.txt", "r")
contenido = archivo.read()  #3Ô∏è‚É£ Lee todo el contenido
print(contenido)
archivo.close()

#!üß™ Ejemplo con with (forma recomendada)

#4Ô∏è‚É£ Con with, no necesitas cerrar manualmente
