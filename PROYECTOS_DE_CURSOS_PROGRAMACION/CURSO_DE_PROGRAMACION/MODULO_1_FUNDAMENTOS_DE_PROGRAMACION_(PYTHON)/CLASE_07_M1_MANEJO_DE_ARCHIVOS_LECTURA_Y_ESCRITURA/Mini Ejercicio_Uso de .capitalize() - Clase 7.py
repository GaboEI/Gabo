# ✅ Mini Ejercicio: Uso de .capitalize() en una lista de nombres
# 1️⃣ Lista de nombres
nombres = ["gAbRiel", "lIliAna", "CriStina"]
# 2️⃣ Recorrer la lista e imprimir el nombre original y capitalizado
for e in nombres:
    print(f"Nombre origuinal: {e}")
    print(f"Nombre capitalizado: {e.capitalize()}")
    print()