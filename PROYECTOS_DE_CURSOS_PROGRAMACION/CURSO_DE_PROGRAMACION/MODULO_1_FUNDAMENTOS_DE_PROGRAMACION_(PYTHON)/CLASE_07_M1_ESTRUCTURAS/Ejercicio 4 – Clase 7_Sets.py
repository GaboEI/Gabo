# ✅ Ejercicio 4: Uso de Sets en Python
# 1️⃣ Crear el set con los valores {1, 2, 3, 4, 5}
numeros = {1, 2, 3, 4, 5}
# 2️⃣ Agregar el número 6 usando .add()
numeros.add(6)
# 3️⃣ Eliminar el número 3 usando .remove()
numeros.remove(3)
# 4️⃣ # Imprimir el set completo
print(f"\nSet completo: {numeros}")
# 4️⃣.1  Imprimir el set completo de manera clara
print("set completo de manera clara")
for elemento in numeros:
    print(f"- {elemento}")