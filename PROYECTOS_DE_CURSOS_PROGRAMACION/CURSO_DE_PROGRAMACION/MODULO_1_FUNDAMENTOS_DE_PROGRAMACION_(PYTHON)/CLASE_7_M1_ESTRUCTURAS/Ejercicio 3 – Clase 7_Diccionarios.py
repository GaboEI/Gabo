# ✅ Ejercicio 3: Uso de Diccionarios en Python
# 1️⃣ Diccionario llamado usuario con las claves "nombre", "edad" y "pais"
usuario = {
    "nombre": "Gabriel",
    "edad": 26,
    "pais": "Cuba"
}
# 2️⃣ Modifico la edad con un nuevo valor 27
usuario["edad"] = 27
# 3️⃣ Agrego una nueva clave "ciudad" con un valor "Camaguey"
usuario["ciudad"] = "Camaguey"
# 4️⃣ Imprimir el diccionario final
print(f"Diccionario Final\n, {usuario}")